package com.project.mohe.domain;

public class DonationVO {
	private int dnt_no;
	private String dnt_name;
	private String dnt_phone;
	private String dnt_amount;
	private String dnt_receipt;
	private String dnt_indate;
	
	public int getDnt_no() {
		return dnt_no;
	}
	public void setDnt_no(int dnt_no) {
		this.dnt_no = dnt_no;
	}
	public String getDnt_name() {
		return dnt_name;
	}
	public void setDnt_name(String dnt_name) {
		this.dnt_name = dnt_name;
	}
	public String getDnt_phone() {
		return dnt_phone;
	}
	public void setDnt_phone(String dnt_phone) {
		this.dnt_phone = dnt_phone;
	}
	public String getDnt_amount() {
		return dnt_amount;
	}
	public void setDnt_amount(String dnt_amount) {
		this.dnt_amount = dnt_amount;
	}
	public String getDnt_receipt() {
		return dnt_receipt;
	}
	public void setDnt_receipt(String dnt_receipt) {
		this.dnt_receipt = dnt_receipt;
	}
	public String getDnt_indate() {
		return dnt_indate;
	}
	public void setDnt_indate(String dnt_indate) {
		this.dnt_indate = dnt_indate;
	}
}
